#include "ft_printf.h"

size_t	ft_nbrlen(intmax_t nbr)
{
	size_t i;

	i = 1;
	while (nbr >= 10 || nbr <= -10)
	{
		nbr /= 10;
		i++;
	}
	return ((nbr < 0) ? i + 1 : i);
}
